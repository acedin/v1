							<h2>How overweight and Obesity are classified?</h2>

							<p><img src="images/bottel_img.png" alt="Bottel" style="float:right"/>Body mass index (BMI)
								is commonly used to classify overweight and <br/>
								obesity in adults. It is defined as the weight in kilograms divided by the<br/>
								square of the height in meters (kg/m2). </p>

							<p><strong>BMI = weight in kilograms / square of the height in meters.</strong></p>

							<p>BMI provides the most useful population-level measure of overweight <br/>
								and obesity as it is the same for both sexes and for all ages of adults. <br/>
								However, it should be considered as a rough guide because it may not <br/>
								correspond to the same degree of fatness in different individuals.</p>

							<p>The World Health Organization (WHO) defines &quot;overweight&quot; as a BMI <br/>
								equal to or more than 25, and &quot;obesity&quot; as a BMI equal to or more <br/>
								than 30. These cut-off points provide a benchmark for individual <br/>
								assessment but there is evidence that risk of chronic disease in <br/>
								populations increases progressively from a BMI of 21.<br/>
							</p>

